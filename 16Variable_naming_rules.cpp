#include<iostream>
using namespace std;
int main(){
    //c++ is a case sensetive language, and reserved names shall not used as a variable name.
    //Pascal case: First letter of each subsequent word is capital ex. BatMan
    string BatMan = "BatMan";

    //Camel case: First letter of first subsequent is small and others are capital ex. wonderWoman
    string wonderWoman = "wonderWoman";

    cout<<BatMan<<endl<<wonderWoman<<endl;
    
    return 0;
}